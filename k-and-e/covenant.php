      Why are we getting married?
	  <br><br>
	  Simply put, marriage is a display of the gospel, the <em> doing </em> of God, to the <em>glory</em> of God. It is the display of God's work through Jesus Christ and his covenant with the Church, who Christ died for at the cross. And while we love one another and want to spend the rest of our lives together (sorta, I guess), we know that only through the foundation of God's covenant with mankind and through a marriage which God has joined, may we truly find love, joy, and peace.
