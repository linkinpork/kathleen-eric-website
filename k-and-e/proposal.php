    On December 22, 2012, Eric and Kathleen journeyed to the Monterey Bay Aquarium. Though Kathleen had been suspicious at first, for it was a rare occasion when the two embarked on such fun journeys, Eric successfully dismissed her suspsicions with his smooth-talking and his choice of attire. Little did Kathleen know that Eric had intentionally worn a gray sweatshirt over a button-down shirt to mislead her. 
    <br><br>
    Their journey was a fun one: they met all sorts of jellyfish, seahorses, and even puffins. Yet every so often, Eric would insist on getting fresh air, claiming that the display glasses were warped and giving him headaches. Little did Kathleen know that Eric was scoping out places outside for the pending proposal. 
    <br><br><br>
    
    <img src="img/proposal6.jpg" alt="proposal6" class="proposalright" align="right"> 

    As their journey neared its end, Eric suggested that they head outside to the 3rd floor balcony one last time to take a photo to commemorate their sea-friend-filled-adventure. Little did Kathleen know that they were being watched.
    <br><br>
    After taking a few photos, Eric asked Kathleen to sit. He then proceeded to read a poem/rap. At this point, Kathleen was still completely convinced that Eric was not proposing. It was not until Eric read the epic line containing the words <i>prose, pros,</i> and <i>propose,</i> that Kathleen realized that, indeed, he was proposing!
    <br><br>
    
    After finishing his poem/rap, Eric asked Kathleen to read a few lines from <i> The Hobbit</i>  that he had underlined and tweaked for the special occasion. &#40;Background: Kathleen had been highly anticipating the movie, even re-reading the book and dreaming that she met Gollum&#41; As Kathleen read, Eric quickly took off his gray sweatshirt to reveal a button-down shirt. Shocked at his sneakiness, Kathleen thanked him for not proposing in a sweatshirt. 
    <br><br>

    With that, Eric knelt down, said some things that Kathleen cannot recall, and asked Kathleen to marry him!
    <br><br>

    Neither Kathleen nor Eric remember whether Kathleen actually said "yes," but she took the ring... and wears the ring... And so, they must be engaged! There were more surprises to follow that night, as the sneakiness of many a friend and loved one was exposed. But those tales can be saved for another time. <br><br><br><br>
